import pandas as pd

# Load the dataset
df = pd.read_csv("IRIS.csv")

# Display basic info
df.info()
df.head()

# Check data types and missing values
print("\nData Types:")
print(df.dtypes)
print("\nMissing Values:")
print(df.isnull().sum())

# Handle missing values (if any)
df = df.dropna()  # Drop missing values
display(df.head())

"""## Basic Data Analysis"""

# Compute basic statistics
df.describe()

# Group by species and compute mean of numerical columns
print("\nMean Values by Species:")
display(df.groupby('species').mean())

# Group by species and compute mean of numerical columns
df.groupby("species").mean()

"""## Data Visualization"""

import matplotlib.pyplot as plt
import seaborn as sns

sns.set_style("whitegrid")

# Line Chart - Sepal Length Trend
plt.figure(figsize=(8,4))
plt.plot(df.index, df["sepal_length"], label="Sepal Length", color="blue")
plt.xlabel("Index")
plt.ylabel("Sepal Length (cm)")
plt.title("Trend of Sepal Length")
plt.legend()
plt.show()

# Bar Chart - Average Petal Length per Species
plt.figure(figsize=(8,4))
sns.barplot(x="species", y="petal_length", data=df, palette="viridis")
plt.title("Average Petal Length per Species")
plt.xlabel("Species")
plt.ylabel("Petal Length (cm)")
plt.show()

# Histogram - Sepal Width Distribution
plt.figure(figsize=(8,4))
sns.histplot(df["sepal_width"], bins=15, kde=True, color="purple")
plt.title("Distribution of Sepal Width")
plt.xlabel("Sepal Width (cm)")
plt.ylabel("Frequency")
plt.show()

# Scatter Plot - Sepal Length vs Petal Length
plt.figure(figsize=(8,4))
sns.scatterplot(x="sepal_length", y="petal_length", hue="species", data=df, palette="coolwarm")
plt.title("Sepal Length vs Petal Length")
plt.xlabel("Sepal Length (cm)")
plt.ylabel("Petal Length (cm)")
plt.legend(title="Species")
plt.show()

"""## Findings

1. **Sepal Length Trend:** The sepal length varies across samples, with no clear increasing or decreasing trend.
2. **Petal Length Differences:** *Iris-setosa* has significantly smaller petal length compared to the other two species.
3. **Sepal Width Distribution:** The histogram suggests a nearly normal distribution for sepal width.
4. **Scatter Plot Insights:** There's a clear distinction between *Iris-setosa* and the other species in sepal vs. petal length.
"""